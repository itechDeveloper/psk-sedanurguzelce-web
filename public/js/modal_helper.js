// modal 1 ************************
const modal_container_1 = document.getElementById("modal_container_1");
const open_1 = document.getElementById("read_more_btn_1");
open_1.addEventListener("click", () => {
  modal_container_1.classList.add("show");
});
const close_1 = document.getElementById("modal_close_btn_1");
const close_X_1 = document.getElementById("modal_close_btn_X_1");
close_1.addEventListener("click", () => {
  modal_container_1.classList.remove("show");
});
close_X_1.addEventListener("click", () => {
  modal_container_1.classList.remove("show");
});

